namespace GanbaroDigital\InstructionPipeline\V1\Checks;

use GanbaroDigital\MissingBits\Checks\Check;
use GanbaroDigital\MissingBits\TypeInspectors\GetClassTypes;
use GanbaroDigital\InstructionPipeline\V1\Interfaces\InstructionBuilder;

/**
 * is $fieldOrVar a valid InstructionBuilder class?
 */
class IsValidInstructionBuilderClass implements Check
{
    /**
     * is $fieldOrVar a valid InstructionBuilder class?
     *
     * @param  mixed $fieldOrVar
     *         the data to be examined
     * @return bool
     *         TRUE if the inspection passes
     *         FALSE otherwise
     */
    public function inspect($fieldOrVar)
    {
        return static::check($fieldOrVar);
    }

    /**
     * is $fieldOrVar a valid InstructionBuilder class?
     *
     * @param  mixed $fieldOrVar
     *         the data to be examined
     * @return bool
     *         TRUE if the inspection passes
     *         FALSE otherwise
     */
    public static function check($fieldOrVar)
    {
        $types = GetClassTypes::from($fieldOrVar);
        if (!isset($types[InstructionBuilder::class])) {
            return false;
        }

        // if we get here, we have run out of things to check
        return true;
    }
}
